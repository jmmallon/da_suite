// Some bands, like Par Lindh, display special characters.  Find an alias, if any.
function find_band_alias ($bandname) {
    global $band_alias;
    if (!empty($band_alias["$bandname"])) {
		$newband = $band_alias["$bandname"];
		return $newband;
		}
	else {
		return $bandname;
	}
}

function find_band_link ($bandname_1) {
	global $link_bands;
	global $link_bands_alt;

	$bandname_1 = str_replace("&amp;", "&", $bandname_1);
	$display_band = find_band_alias($bandname_1);
	$bandname = person_name ($bandname_1);
//    echo ("Band: $bandname; link: $link_bands[$bandname]<br>");
	if (!empty($link_bands["$bandname"])) {
//	    echo ("hit!");
	    $newband = $link_bands["$bandname"];
		return "<a href=$newband target=\"_blank\">$display_band</a>";
		}
	else {
		if (!empty($link_bands["$bandname_1"])) {
//	        echo ("hit!");
	    	$newband = $link_bands["$bandname_1"];
			return "<a href=$newband target=\"_blank\">$display_band</a>";
		}
		else {
	    	if (!empty($link_bands_alt["$bandname_1"])) {
	    		$newband = $link_bands[$link_bands_alt["$bandname"]];
				return "<a href=$newband target=\"_blank\">$display_band</a>";
			}
			else {
//	    		echo ("miss.");
				return $display_band;
			}
		}
	}
}

function person_name ($bandname) {
	$name = explode (" ", $bandname);
	if (count ($name) == 2) {
	  return "$name[1], $name[0]";
	}
	else {
	  return $bandname;
	}
}
	

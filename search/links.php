 function makeitalink ($song) {

	// grab the band name
	$song = preg_replace('/\s*$/', '', $song);
    	$song = preg_replace('/`([^`]*)`/', '<b><i>$1</i></b>', $song);
    	// $song = str_replace(' `', ' <b><i>', $song);
	$pos = strpos ($song, " - ");
	$bandname = substr ($song, 0, $pos);
	//echo ("Bandname is .$bandname.");
	$newbandname = find_band_link ($bandname);
    return substr_replace ("$song", $newbandname, 0, $pos);
}

